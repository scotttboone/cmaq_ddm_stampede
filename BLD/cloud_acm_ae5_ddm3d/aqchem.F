        WETFAC = PRCRATE * FRACLIQ * DTW( 0 ) * SEC2HR
       
        DO LIQ = 1, NLIQS
          WETDEP( LIQ ) = WETDEP( LIQ ) + LIQUID( LIQ ) * WETFAC
#ifdef sens
c sln =-=-=-=-=-=-=  DDM-3D
          DO NP = 1, NPMAX
            S_WETDEP( NP,LIQ ) = S_WETDEP( NP,LIQ ) + S_LIQUID( NP,LIQ ) * WETFAC
          END DO
c sln =-=-=-=-=-=-=  DDM-3D
#endif
        END DO

        TIMEW = TIMEW + DTW( 0 )

C...Return to make additional calculations

        GO TO 20
      END IF

