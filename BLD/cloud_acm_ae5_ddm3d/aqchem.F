#ifdef sens
c sln =-=-=-=-=-=-=  DDM-3D
        DO NP = 1, NPMAX
           DO IOX = 1, NUMOX
            S_DSIVDT(NP, IOX) = MIN(S_DSIVDT(NP,IOX),(-1.5*DSIVDT(IOX)))
            S_DSIVDT(NP, IOX) = MAX(S_DSIVDT(NP,IOX),( 1.5*DSIVDT(IOX)))
