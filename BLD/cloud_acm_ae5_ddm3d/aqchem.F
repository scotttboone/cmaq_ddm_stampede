           IF ( S_TGLY( NP ) .GE. 0.0 ) THEN
             S_DGLYDT( NP ) = MAX( S_DGLYDT(NP), -S_TGLY(NP)/DTW(0) )
           ELSE
             S_DGLYDT( NP ) = MIN( S_DGLYDT(NP), -S_TGLY(NP)/DTW(0) )
           END IF

           IF ( TMGLY .NE. 0.0 ) THEN
             MGLYTEMP = S_TMGLY(NP) * ( -DMGLYDT / TMGLY )
           ELSE
             MGLYTEMP = 0.0    
           ENDIF

           IF ( TGLY .NE. 0.0 ) THEN
             GLYTEMP  = S_TGLY(NP)  * ( -DGLYDT  / TGLY )
           ELSE
             GLYTEMP  = 0.0
           ENDIF
          
           IF ( MGLYTEMP .GE. 0.0 ) THEN
             S_DMGLYDT( NP ) = MAX( S_DMGLYDT( NP ),  0.0d0 )
             S_DMGLYDT( NP ) = MIN( S_DMGLYDT( NP ), -DBLE(MGLYTEMP) )
           ELSE 
             S_DMGLYDT( NP ) = MIN( S_DMGLYDT( NP ),  0.0d0 )
             S_DMGLYDT( NP ) = MAX( S_DMGLYDT( NP ), -DBLE(MGLYTEMP) )
           ENDIF

           IF ( GLYTEMP .GE. 0.0 ) THEN
             S_DGLYDT( NP ) = MAX( S_DGLYDT( NP ),  0.0d0 )
             S_DGLYDT( NP ) = MIN( S_DGLYDT( NP ), -DBLE(GLYTEMP) )
           ELSE
             S_DGLYDT( NP ) = MIN( S_DGLYDT( NP ),  0.0d0 )
             S_DGLYDT( NP ) = MAX( S_DGLYDT( NP ), -DBLE(GLYTEMP) )
           ENDIF

           S_DGLY1( NP )  = S_DGLY1( NP )  + DTW( 0 ) * S_DGLYDT( NP )
           S_DMGLY1( NP ) = S_DMGLY1( NP ) + DTW( 0 ) * S_DMGLYDT( NP )

           S_DORGC( NP ) = S_DORGC( NP )
     &           - ( 0.04 * ( S_DGLYDT( NP ) + S_DMGLYDT( NP ) ) * DTW( 0 ) )

        END DO
c sln =-=-=-=-=-=-=  DDM-3D
#endif

