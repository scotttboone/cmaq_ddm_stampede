#ifdef sens
c sln =-=-=-=-=-=-=  DDM-3D
        DO NP = 1, NPMAX
c this needs lots of limits to prevent noise in small sensitivities from getting out of control

           IF ( S_TMGLY( NP ) .GE. 0.0 ) THEN
             S_DMGLYDT( NP ) = MAX( S_DMGLYDT(NP), -S_TMGLY(NP)/DTW(0) )
